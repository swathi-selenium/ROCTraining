package com.revature.Scanner;

public class Person {
	

		private String firstName;
		private String lastName;
		private int age;
		private double salary;
		private String favoriteColor;
		
		public Person() {
			super();
		}

		public Person(String firstName, String lastName, int age, double salary, String favoriteColor) {
			super();
			this.firstName = firstName;
			this.lastName = lastName;
			this.age = age;
			this.salary = salary;
			this.favoriteColor = favoriteColor;
		}

		@Override
		public String toString() {
			return "Person [firstName=" + firstName + ", lastName=" + lastName + ", age=" + age + ", salary=" + salary
					+ ", favoriteColor=" + favoriteColor + "]";
		}

		@Override
		public int hashCode() {
			final int prime = 31;
			int result = 1;
			result = prime * result + age;
			result = prime * result + ((favoriteColor == null) ? 0 : favoriteColor.hashCode());
			result = prime * result + ((firstName == null) ? 0 : firstName.hashCode());
			result = prime * result + ((lastName == null) ? 0 : lastName.hashCode());
			long temp;
			temp = Double.doubleToLongBits(salary);
			result = prime * result + (int) (temp ^ (temp >>> 32));
			return result;
		}

		@Override
		public boolean equals(Object obj) {
			if (this == obj)
				return true;
			if (obj == null)
				return false;
			if (getClass() != obj.getClass())
				return false;
			Person other = (Person) obj;
			if (age != other.age)
				return false;
			if (favoriteColor == null) {
				if (other.favoriteColor != null)
					return false;
			} else if (!favoriteColor.equals(other.favoriteColor))
				return false;
			if (firstName == null) {
				if (other.firstName != null)
					return false;
			} else if (!firstName.equals(other.firstName))
				return false;
			if (lastName == null) {
				if (other.lastName != null)
					return false;
			} else if (!lastName.equals(other.lastName))
				return false;
			if (Double.doubleToLongBits(salary) != Double.doubleToLongBits(other.salary))
				return false;
			return true;
		}

		public String getFirstName() {
			return firstName;
		}

		public void setFirstName(String firstName) {
			this.firstName = firstName;
		}

		public String getLastName() {
			return lastName;
		}

		public void setLastName(String lastName) {
			this.lastName = lastName;
		}

		public int getAge() {
			return age;
		}

		public void setAge(int age) {
			this.age = age;
		}

		public double getSalary() {
			return salary;
		}

		public void setSalary(double salary) {
			this.salary = salary;
		}

		public String getFavoriteColor() {
			return favoriteColor;
		}

		public void setFavoriteColor(String favoriteColor) {
			this.favoriteColor = favoriteColor;
		}
		
	}

